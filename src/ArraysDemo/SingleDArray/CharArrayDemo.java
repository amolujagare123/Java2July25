package ArraysDemo.SingleDArray;

public class CharArrayDemo {

    public static void main(String[] args) {

        char[] c = { 'A', 'B', 'C', 'D', 'E' };

        for(char ch : c)
        {
            System.out.println(ch);
        }

        String[] stArr = { "Data1", "Data2", "Data3", "Data4" };

        for(String str : stArr)
        {
            System.out.println(str);
        }

    }
}
